class DES:
    
    def __init__ (self, key):
        self.key = key
        self.round_keys = self.generate_round_keys()
        
    def _permute (self, block, table):
        return [block[i - 1] for i in table]
    
    def _s_box_substitution (self, block, s_boxes):
        output = []
        for i in range(8):
            segment = block[i * 6:(i + 1) * 6]  # Extract 6-bit segment
            row = (segment[0] << 1) | segment[5]  # Row from outer bits
            col = int("".join(map(str, segment[1:5])), 2)  # Column from inner bits
            value = s_boxes[i][row][col]  # Lookup S-Box value
            output.extend([int(b) for b in f"{value:04b}"])  # Convert to 4-bit binary
        return output
    
    def _feistel_function(self, right, round_key, s_boxes, p_table):
        # Step 1: Expansion
        expanded_right = self._permute(right, E)

        # Step 2: XOR with round key
        xor_result = [bit ^ key_bit for bit, key_bit in zip(expanded_right, round_key)]

        # Step 3: S-Box substitution
        s_box_output = self._s_box_substitution(xor_result, s_boxes)
        boxes = []
        for x in range(len(s_box_output) // 6):
            boxes.append(xor_result[x * 6:(x + 1) * 6])
        print(boxes)
        
        # Step 4: Permutation
        final_output = self._permute(s_box_output, p_table)
        return final_output
    
    def _des_full(self, input_text, keys, s_boxes, p_table):
        # Step 1: Initial Permutation
        permuted_text = self._permute(input_text, IP)
        left, right = permuted_text[:32], permuted_text[32:]

        # Step 2: 16 Rounds
        for i in range(16):
            round_key = keys[i]
            new_right = self._feistel_function(right, round_key, s_boxes, p_table)
            new_right = [l ^ r for l, r in zip(left, new_right)]  # XOR with left
            left = right  # Swap halves
            right = new_right

        # Step 3: Final Permutation
        pre_output = right + left  # Combine halves (note the swap)
        final_output = self._permute(pre_output, FP)
        return final_output
    
    def generate_round_keys(self):
        
        def left_circular_shift(block, shifts):
            return block[shifts:] + block[:shifts]
        
        # Apply PC-1
        permuted_key = self._permute(self.key, PC1)
        left, right = permuted_key[:28], permuted_key[28:]

        # Shift schedule
        shift_schedule = [1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1]

        # Generate round keys
        round_keys = []
        for shifts in shift_schedule:
            left = left_circular_shift(left, shifts)
            right = left_circular_shift(right, shifts)
            combined = left + right
            round_keys.append(self._permute(combined, PC2))  # Apply PC-2

        return round_keys
    
    def encrypt(self, plaintext):
        if len(plaintext) < 64:
            raise ValueError("Not correct number of bits")
        return self._des_full(plaintext, self.round_keys, S_BOXES, P)
    
    def decrypt(self, ciphertext):
        if len(ciphertext) < 64:
            raise ValueError("Not correct number of bits")
        return self._des_full(ciphertext, self.round_keys[::-1], S_BOXES, P)

